export function getDefaultShopsForLanguage(language: string): Shop[] {
  const shopsData = defaultShopsData[language] || defaultShopsData.en;
  return shopsData.map((shop, index) => ({
    ...shop,
    id: `default-${language}-${index}`,
    multilingualData: {
      [language]: {
        name: shop.name,
        icon: shop.icon,
        color: shop.color || "#3B82F6",
      }
    }
  }));
}